def fetch_uniprot_sequences(max_sequences: int, use_download: bool) -> list[str]:
    if not use_download:
        print("Skipping UniProt download. Using fallback sequences.")
        return FALLBACK_SEQUENCES

    url = "https://rest.uniprot.org/uniprotkb/search"
    queries = [
        "(gene:luxR OR gene_exact:luxR OR protein_name:LuxR) AND taxonomy_id:2 AND length:[150 TO 300]",
        "(gene:luxR OR protein_name:LuxR OR cc_function:LuxR) AND taxonomy_id:2 AND length:[150 TO 300]",
        "luxR AND taxonomy_id:2 AND length:[150 TO 300]",
    ]
    fields = "accession,id,protein_name,gene_names,organism_name,length,sequence"

    print("\nDownloading LuxR protein sequences from UniProt...")

    session = requests.Session()
    session.headers.update(
        {
            "User-Agent": "luxr-colab-protein-generator/1.1 "
            "(https://colab.research.google.com; educational script)"
        }
    )

    def get_json(params: dict[str, str], allow_insecure_ssl: bool = False) -> dict:
        verify_arg: str | bool = False if allow_insecure_ssl else certifi.where()
        if allow_insecure_ssl:
            urllib3.disable_warnings(urllib3.exceptions.InsecureRequestWarning)
        response = session.get(url, params=params, timeout=(15, 90), verify=verify_arg)
        response.raise_for_status()
        return response.json()

    sequences: list[str] = []
    seen: set[str] = set()
    ssl_retry_used = False
    candidate_limit = min(max(max_sequences * 4, 100), 500)

    for query in queries:
        params = {
            "query": query,
            "format": "json",
            "fields": fields,
            "size": str(candidate_limit),
        }

        try:
            data = get_json(params)
        except requests.exceptions.SSLError as exc:
            print(f"SSL verification failed while contacting UniProt: {exc}")
            print("Retrying UniProt once without certificate verification for this download.")
            ssl_retry_used = True
            try:
                data = get_json(params, allow_insecure_ssl=True)
            except Exception as retry_exc:
                print(f"UniProt SSL retry failed: {retry_exc}")
                continue
        except Exception as exc:
            print(f"UniProt query failed: {exc}")
            continue

        added = 0
        for entry in data.get("results", []):
            sequence = entry.get("sequence", {}).get("value", "")
            if sequence and sequence not in seen:
                sequences.append(sequence)
                seen.add(sequence)
                added += 1
            if len(sequences) >= candidate_limit:
                break

        if added:
            print(f"UniProt query returned {added} new sequence candidates: {query}")

        if len(sequences) >= candidate_limit:
            break

    if ssl_retry_used:
        print("Note: SSL fallback was used. In Colab this usually is not needed.")

    valid = set(AMINO_ACIDS)
    clean = [seq for seq in sequences if seq and all(char in valid for char in seq)]
    if not clean:
        print("UniProt returned no clean amino acid sequences. Using fallback sequences.")
        return FALLBACK_SEQUENCES

    diverse: list[str] = []
    for seq in clean:
        is_duplicate = False
        for other in diverse:
            matches = sum(1 for a, b in zip(seq, other) if a == b)
            similarity = matches / max(len(seq), len(other))
            if similarity > 0.90:
                is_duplicate = True
                break
        if not is_duplicate:
            diverse.append(seq)
            if len(diverse) >= max_sequences:
                break

    if not diverse:
        return FALLBACK_SEQUENCES

    print(f"Loaded {len(diverse)} diverse training sequences.")
    return diverse


def encode(sequence: str) -> list[int]:
    return [BOS_IDX] + [CHAR_TO_ID[char] for char in sequence if char in CHAR_TO_ID] + [EOS_IDX]


def decode(token_ids: list[int]) -> str:
    skip = {PAD_IDX, BOS_IDX, EOS_IDX}
    return "".join(ID_TO_CHAR[idx] for idx in token_ids if idx not in skip and idx in ID_TO_CHAR)


class ProteinWindowDataset(Dataset):
    def __init__(self, sequences: list[str], block_size: int):
        self.windows: list[list[int]] = []
        self.block_size = block_size

        stride = max(1, block_size // 3)
        window_len = block_size + 1

        for sequence in sequences:
            tokens = encode(sequence)
            if len(tokens) <= window_len:
                padded = tokens + [PAD_IDX] * (window_len - len(tokens))
                self.windows.append(padded)
                continue

            starts = list(range(0, len(tokens) - block_size, stride))
            last_start = max(0, len(tokens) - window_len)
            if last_start not in starts:
                starts.append(last_start)

            for start in starts:
                window = tokens[start : start + window_len]
                if len(window) == window_len:
                    self.windows.append(window)

        if not self.windows:
            raise ValueError("No training windows were created. Try a smaller block size.")

    def __len__(self) -> int:
        return len(self.windows)

    def __getitem__(self, index: int) -> tuple[torch.Tensor, torch.Tensor]:
        window = torch.tensor(self.windows[index], dtype=torch.long)
        return window[:-1], window[1:]


class EvoBlock(nn.Module):
    def __init__(self, cfg: Config):
        super().__init__()
        self.n_heads = cfg.n_heads
        self.n_embed = cfg.n_embed
        self.head_size = cfg.n_embed // cfg.n_heads

        self.query = nn.Linear(cfg.n_embed, cfg.n_embed, bias=False)
        self.key = nn.Linear(cfg.n_embed, cfg.n_embed, bias=False)
        self.value = nn.Linear(cfg.n_embed, cfg.n_embed, bias=False)
        self.attn_dropout = nn.Dropout(cfg.dropout)
        self.resid_dropout = nn.Dropout(cfg.dropout)

        self.outer_left = nn.Linear(cfg.n_embed, cfg.pair_embed)
        self.outer_right = nn.Linear(cfg.n_embed, cfg.pair_embed)
        self.pair_ff = nn.Sequential(
            nn.Linear(cfg.pair_embed, cfg.pair_embed),
            nn.GELU(),
            nn.Linear(cfg.pair_embed, cfg.pair_embed),
        )
        self.pair_to_bias = nn.Linear(cfg.pair_embed, cfg.n_heads)

        self.seq_ff = nn.Sequential(
            nn.Linear(cfg.n_embed, 2 * cfg.n_embed),
            nn.GELU(),
            nn.Dropout(cfg.dropout),
            nn.Linear(2 * cfg.n_embed, cfg.n_embed),
            nn.Dropout(cfg.dropout),
        )
        self.seq_norm_1 = nn.LayerNorm(cfg.n_embed)
        self.seq_norm_2 = nn.LayerNorm(cfg.n_embed)
        self.pair_norm = nn.LayerNorm(cfg.pair_embed)

    def forward(self, sequence: torch.Tensor, pair: torch.Tensor) -> tuple[torch.Tensor, torch.Tensor]:
        batch, steps, _ = sequence.shape

        left = self.outer_left(sequence)
        right = self.outer_right(sequence)
        pair_update = left[:, :, None, :] * right[:, None, :, :]
        pair = self.pair_norm(pair + self.pair_ff(pair_update))

        normed = self.seq_norm_1(sequence)
        q = self.query(normed).view(batch, steps, self.n_heads, self.head_size).transpose(1, 2)
        k = self.key(normed).view(batch, steps, self.n_heads, self.head_size).transpose(1, 2)
        v = self.value(normed).view(batch, steps, self.n_heads, self.head_size).transpose(1, 2)

        scores = (q @ k.transpose(-2, -1)) / math.sqrt(self.head_size)
        scores = scores + self.pair_to_bias(pair).permute(0, 3, 1, 2)

        causal_mask = torch.tril(torch.ones(steps, steps, device=sequence.device, dtype=torch.bool))
        scores = scores.masked_fill(~causal_mask[None, None, :, :], float("-inf"))

        attn = F.softmax(scores, dim=-1)
        attn = self.attn_dropout(attn)
        update = (attn @ v).transpose(1, 2).contiguous().view(batch, steps, self.n_embed)

        sequence = sequence + self.resid_dropout(update)
        sequence = sequence + self.seq_ff(self.seq_norm_2(sequence))
        return sequence, pair


class ProteinGeneratorModel(nn.Module):
    def __init__(self, cfg: Config):
        super().__init__()
        self.cfg = cfg
        self.token_embedding = nn.Embedding(len(VOCAB), cfg.n_embed)
        self.position_embedding = nn.Embedding(cfg.block_size, cfg.n_embed)
        self.pair_seed = nn.Parameter(torch.randn(1, cfg.block_size, cfg.block_size, cfg.pair_embed) * 0.02)
        self.blocks = nn.ModuleList([EvoBlock(cfg) for _ in range(cfg.n_layers)])
        self.final_norm = nn.LayerNorm(cfg.n_embed)
        self.output_head = nn.Linear(cfg.n_embed, len(VOCAB), bias=False)
        self.output_head.weight = self.token_embedding.weight

    def forward(
        self, input_ids: torch.Tensor, targets: torch.Tensor | None = None
    ) -> tuple[torch.Tensor, torch.Tensor | None]:
        batch, steps = input_ids.shape
        if steps > self.cfg.block_size:
            raise ValueError(f"Input length {steps} exceeds block size {self.cfg.block_size}.")

        token_emb = self.token_embedding(input_ids)
        pos_ids = torch.arange(steps, device=input_ids.device)
        pos_emb = self.position_embedding(pos_ids)
        sequence = token_emb + pos_emb

        pair = self.pair_seed[:, :steps, :steps, :].expand(batch, steps, steps, self.cfg.pair_embed)
        for block in self.blocks:
            sequence, pair = block(sequence, pair)

        logits = self.output_head(self.final_norm(sequence))
        loss = None
        if targets is not None:
            loss = F.cross_entropy(
                logits.reshape(-1, len(VOCAB)),
                targets.reshape(-1),
                ignore_index=PAD_IDX,
            )
        return logits, loss


def make_loaders(dataset: Dataset, cfg: Config) -> tuple[DataLoader, DataLoader | None]:
    if len(dataset) == 1:
        train_dataset = dataset
        val_dataset = None
    else:
        train_size = max(1, int(0.9 * len(dataset)))
        val_size = len(dataset) - train_size
        if val_size == 0:
            train_size -= 1
            val_size = 1
        train_dataset, val_dataset = torch.utils.data.random_split(
            dataset,
            [train_size, val_size],
            generator=torch.Generator().manual_seed(cfg.seed),
        )

    train_loader = DataLoader(
        train_dataset,
        batch_size=min(cfg.batch_size, len(train_dataset)),
        shuffle=True,
        drop_last=False,
    )
    val_loader = None
    if val_dataset is not None and len(val_dataset) > 0:
        val_loader = DataLoader(
            val_dataset,
            batch_size=min(cfg.batch_size, len(val_dataset)),
            shuffle=False,
            drop_last=False,
        )
    return train_loader, val_loader


def clear_cuda_memory() -> None:
    gc.collect()
    if torch.cuda.is_available():
        torch.cuda.empty_cache()
        try:
            torch.cuda.ipc_collect()
        except Exception:
            pass


def is_cuda_oom(exc: BaseException) -> bool:
    text = str(exc).lower()
    return isinstance(exc, RuntimeError) and (
        "cuda out of memory" in text or "outofmemoryerror" in text or "out of memory" in text
    )


def make_grad_scaler(enabled: bool):
    try:
        return torch.amp.GradScaler("cuda", enabled=enabled)
    except Exception:
        return torch.cuda.amp.GradScaler(enabled=enabled)


def autocast_cuda(enabled: bool):
    try:
        return torch.amp.autocast("cuda", enabled=enabled)
    except Exception:
        return torch.cuda.amp.autocast(enabled=enabled)


def train_batch_with_oom_retry(
    model: ProteinGeneratorModel,
    optimizer: torch.optim.Optimizer,
    scaler,
    x: torch.Tensor,
    y: torch.Tensor,
    use_amp: bool,
) -> float:
    micro_batch_size = x.size(0)

    while micro_batch_size >= 1:
        try:
            losses: list[float] = []
            for start in range(0, x.size(0), micro_batch_size):
                xb = x[start : start + micro_batch_size]
                yb = y[start : start + micro_batch_size]

                optimizer.zero_grad(set_to_none=True)
                with autocast_cuda(use_amp):
                    _, loss = model(xb, yb)
                if loss is None:
                    continue

                if use_amp:
                    scaler.scale(loss).backward()
                    scaler.unscale_(optimizer)
                    torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                    scaler.step(optimizer)
                    scaler.update()
                else:
                    loss.backward()
                    torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                    optimizer.step()

                losses.append(float(loss.item()))

            if not losses:
                return float("nan")
            return float(np.mean(losses))

        except RuntimeError as exc:
            if not is_cuda_oom(exc):
                raise

            optimizer.zero_grad(set_to_none=True)
            clear_cuda_memory()

            if micro_batch_size == 1:
                raise RuntimeError(
                    "CUDA ran out of memory even at micro-batch size 1. "
                    "Lower Context/block size first, then Pair embedding size, "
                    "Sequence embedding size, or Transformer layers."
                ) from exc

            next_size = max(1, micro_batch_size // 2)
            print(f"\nCUDA OOM on batch size {micro_batch_size}. Retrying with micro-batch size {next_size}.")
            micro_batch_size = next_size

    return float("nan")


def eval_batch_with_oom_retry(
    model: ProteinGeneratorModel,
    x: torch.Tensor,
    y: torch.Tensor,
    use_amp: bool,
) -> float:
    micro_batch_size = x.size(0)

    while micro_batch_size >= 1:
        try:
            losses: list[float] = []
            with torch.no_grad():
                for start in range(0, x.size(0), micro_batch_size):
                    xb = x[start : start + micro_batch_size]
                    yb = y[start : start + micro_batch_size]
                    with autocast_cuda(use_amp):
                        _, loss = model(xb, yb)
                    if loss is not None:
                        losses.append(float(loss.item()))
            if not losses:
                return float("nan")
            return float(np.mean(losses))

        except RuntimeError as exc:
            if not is_cuda_oom(exc):
                raise

            clear_cuda_memory()

            if micro_batch_size == 1:
                raise RuntimeError(
                    "CUDA ran out of memory during validation even at micro-batch size 1. "
                    "Lower Context/block size first, then Pair embedding size, "
                    "Sequence embedding size, or Transformer layers."
                ) from exc

            next_size = max(1, micro_batch_size // 2)
            print(f"\nCUDA OOM during validation on batch size {micro_batch_size}. Retrying with {next_size}.")
            micro_batch_size = next_size

    return float("nan")


def train_model(
    model: ProteinGeneratorModel,
    train_loader: DataLoader,
    val_loader: DataLoader | None,
    cfg: Config,
    device: torch.device,
) -> dict[str, list[float]]:
    optimizer = torch.optim.AdamW(model.parameters(), lr=cfg.learning_rate)
    use_amp = device.type == "cuda" and cfg.mixed_precision
    scaler = make_grad_scaler(use_amp)
    history = {"train": [], "val": []}

    print(f"\nTraining on {len(train_loader.dataset)} windows.")
    if val_loader is not None:
        print(f"Validating on {len(val_loader.dataset)} windows.")
    if use_amp:
        print("CUDA mixed precision is enabled to reduce GPU memory use.")

    for epoch in range(1, cfg.epochs + 1):
        model.train()
        train_losses: list[float] = []
        progress = tqdm(train_loader, desc=f"Epoch {epoch}/{cfg.epochs}", leave=False)
        for x, y in progress:
            x = x.to(device)
            y = y.to(device)

            loss_value = train_batch_with_oom_retry(model, optimizer, scaler, x, y, use_amp)
            if not math.isnan(loss_value):
                train_losses.append(loss_value)
            loss_text = f"{np.mean(train_losses):.4f}" if train_losses else "n/a"
            progress.set_postfix(loss=loss_text)

        model.eval()
        val_losses: list[float] = []
        if val_loader is not None:
            for x, y in val_loader:
                x = x.to(device)
                y = y.to(device)
                loss_value = eval_batch_with_oom_retry(model, x, y, use_amp)
                if not math.isnan(loss_value):
                    val_losses.append(loss_value)

        train_loss = float(np.mean(train_losses)) if train_losses else float("nan")
        val_loss = float(np.mean(val_losses)) if val_losses else float("nan")
        history["train"].append(train_loss)
        history["val"].append(val_loss)

        val_text = f"{val_loss:.4f}" if not math.isnan(val_loss) else "n/a"
        print(f"Epoch {epoch:02d} | Train loss: {train_loss:.4f} | Val loss: {val_text}")

    return history


