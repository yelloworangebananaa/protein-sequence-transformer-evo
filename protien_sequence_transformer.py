def sample_next_token(logits: torch.Tensor, temperature: float, top_k: int) -> int:
    logits = logits.clone()
    logits[PAD_IDX] = float("-inf")
    logits[BOS_IDX] = float("-inf")

    logits = logits / max(temperature, 1e-6)
    if top_k > 0:
        k = min(top_k, logits.numel())
        values, indices = torch.topk(logits, k)
        probs = F.softmax(values, dim=-1)
        choice = torch.multinomial(probs, num_samples=1)
        return int(indices[choice].item())

    probs = F.softmax(logits, dim=-1)
    return int(torch.multinomial(probs, num_samples=1).item())


def generate_sequence(model: ProteinGeneratorModel, cfg: Config, device: torch.device) -> str:
    model.eval()
    token_ids = [BOS_IDX]
    use_amp = device.type == "cuda" and cfg.mixed_precision

    with torch.no_grad():
        while len(token_ids) - 1 < cfg.max_output_length:
            context = token_ids[-cfg.block_size :]
            x = torch.tensor([context], dtype=torch.long, device=device)
            with autocast_cuda(use_amp):
                logits, _ = model(x)
            next_logits = logits[0, -1, :]

            if len(token_ids) - 1 < cfg.min_output_length:
                next_logits = next_logits.clone()
                next_logits[EOS_IDX] = float("-inf")

            next_id = sample_next_token(next_logits, cfg.temperature, cfg.top_k)
            if next_id == EOS_IDX:
                break
            token_ids.append(next_id)

    return decode(token_ids)


def aa_composition(sequence: str) -> str:
    counts = Counter(sequence)
    most_common = counts.most_common(5)
    return ", ".join(f"{aa}:{count}" for aa, count in most_common)


def amino_acid_profile(sequences: list[str]) -> dict[str, float]:
    counts = Counter()
    total = 0
    for sequence in sequences:
        counts.update(char for char in sequence if char in AMINO_ACIDS)
        total += sum(1 for char in sequence if char in AMINO_ACIDS)

    if total == 0:
        return {aa: 1.0 / len(AMINO_ACIDS) for aa in AMINO_ACIDS}

    return {aa: counts[aa] / total for aa in AMINO_ACIDS}


def max_repeat_fraction(sequence: str) -> float:
    if not sequence:
        return 1.0

    longest = 1
    current = 1
    for prev, char in zip(sequence, sequence[1:]):
        if char == prev:
            current += 1
            longest = max(longest, current)
        else:
            current = 1
    return longest / len(sequence)


def sequence_model_loss(
    model: ProteinGeneratorModel,
    sequence: str,
    cfg: Config,
    device: torch.device,
) -> float:
    tokens = encode(sequence)
    if len(tokens) < 2:
        return float("inf")

    weighted_losses: list[float] = []
    token_counts: list[int] = []
    use_amp = device.type == "cuda" and cfg.mixed_precision

    model.eval()
    with torch.no_grad():
        for start in range(0, len(tokens) - 1, cfg.block_size):
            stop = min(start + cfg.block_size, len(tokens) - 1)
            x_ids = tokens[start:stop]
            y_ids = tokens[start + 1 : start + 1 + len(x_ids)]
            if not x_ids or len(x_ids) != len(y_ids):
                continue

            x = torch.tensor([x_ids], dtype=torch.long, device=device)
            y = torch.tensor([y_ids], dtype=torch.long, device=device)
            with autocast_cuda(use_amp):
                logits, _ = model(x)
            loss = F.cross_entropy(logits.reshape(-1, len(VOCAB)), y.reshape(-1))
            weighted_losses.append(float(loss.item()) * len(y_ids))
            token_counts.append(len(y_ids))

    if not token_counts:
        return float("inf")
    return sum(weighted_losses) / sum(token_counts)


def rank_generated_sequences(
    model: ProteinGeneratorModel,
    generated_sequences: list[str],
    training_sequences: list[str],
    cfg: Config,
    device: torch.device,
) -> list[GeneratedCandidate]:
    reference_profile = amino_acid_profile(training_sequences)
    training_lengths = np.array([len(seq) for seq in training_sequences], dtype=float)
    target_length = float(np.mean(training_lengths)) if len(training_lengths) else cfg.max_output_length
    length_spread = float(np.std(training_lengths)) if len(training_lengths) else 1.0
    length_spread = max(length_spread, 1.0)

    ranked: list[tuple[float, GeneratedCandidate]] = []
    seen: set[str] = set()

    for sequence in generated_sequences:
        model_loss = sequence_model_loss(model, sequence, cfg, device)
        length_penalty = abs(len(sequence) - target_length) / length_spread

        profile = amino_acid_profile([sequence])
        composition_distance = sum(abs(profile[aa] - reference_profile[aa]) for aa in AMINO_ACIDS)

        repeat_fraction = max_repeat_fraction(sequence)
        repeat_penalty = max(0.0, repeat_fraction - 0.08)
        duplicate_penalty = 2.0 if sequence in seen else 0.0
        seen.add(sequence)

        rank_loss = (
            model_loss
            + 0.35 * length_penalty
            + 2.50 * composition_distance
            + 4.00 * repeat_penalty
            + duplicate_penalty
        )
        score = 100.0 / (1.0 + rank_loss)

        candidate = GeneratedCandidate(
            rank=0,
            sequence=sequence,
            score=score,
            model_loss=model_loss,
            length_penalty=length_penalty,
            composition_distance=composition_distance,
            repeat_penalty=repeat_penalty,
            duplicate_penalty=duplicate_penalty,
        )
        ranked.append((rank_loss, candidate))

    ranked.sort(key=lambda item: item[0])
    output: list[GeneratedCandidate] = []
    for rank, (_, candidate) in enumerate(ranked, 1):
        output.append(
            GeneratedCandidate(
                rank=rank,
                sequence=candidate.sequence,
                score=candidate.score,
                model_loss=candidate.model_loss,
                length_penalty=candidate.length_penalty,
                composition_distance=candidate.composition_distance,
                repeat_penalty=candidate.repeat_penalty,
                duplicate_penalty=candidate.duplicate_penalty,
            )
        )
    return output


def save_outputs(candidates: list[GeneratedCandidate], output_dir: Path) -> tuple[Path, Path]:
    fasta_path = output_dir / "luxr_generated_sequences.fasta"
    csv_path = output_dir / "luxr_generated_sequences.csv"

    with fasta_path.open("w", encoding="utf-8") as fasta:
        for candidate in candidates:
            sequence = candidate.sequence
            fasta.write(
                f">rank_{candidate.rank}|generated_luxr_{candidate.rank}|"
                f"score={candidate.score:.2f}|model_loss={candidate.model_loss:.4f}|"
                f"length={len(sequence)}\n"
            )
            fasta.write("\n".join(textwrap.wrap(sequence, width=80)) + "\n")

    with csv_path.open("w", encoding="utf-8") as csv:
        csv.write(
            "rank,id,score,model_loss,length,length_penalty,"
            "composition_distance,repeat_penalty,duplicate_penalty,sequence\n"
        )
        for candidate in candidates:
            sequence = candidate.sequence
            csv.write(
                f"{candidate.rank},generated_luxr_{candidate.rank},"
                f"{candidate.score:.4f},{candidate.model_loss:.6f},{len(sequence)},"
                f"{candidate.length_penalty:.6f},{candidate.composition_distance:.6f},"
                f"{candidate.repeat_penalty:.6f},{candidate.duplicate_penalty:.6f},{sequence}\n"
            )

    return fasta_path, csv_path


def print_final_sequences(candidates: list[GeneratedCandidate]) -> None:
    print("\n=== FINAL RECOMMENDED OUTPUT: BEST TO LOWEST ===")
    for candidate in candidates:
        sequence = candidate.sequence
        print(
            f"\n>rank_{candidate.rank} | score={candidate.score:.2f} | "
            f"model_loss={candidate.model_loss:.4f} | length={len(sequence)} | "
            f"top amino acids: {aa_composition(sequence)}"
        )
        print("\n".join(textwrap.wrap(sequence, width=80)))


def main() -> None:
    cfg = get_config()
    set_seed(cfg.seed)
    clear_cuda_memory()

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"\nEngine bound to: {device}")

    sequences = fetch_uniprot_sequences(cfg.num_sequences, cfg.download_uniprot)
    dataset = ProteinWindowDataset(sequences, cfg.block_size)
    train_loader, val_loader = make_loaders(dataset, cfg)

    try:
        model = ProteinGeneratorModel(cfg).to(device)
    except RuntimeError as exc:
        if device.type != "cuda" or not is_cuda_oom(exc):
            raise
        print("\nCUDA ran out of memory while creating the model.")
        print("Switching this run to CPU. For a faster GPU run, lower block size or model size.")
        clear_cuda_memory()
        device = torch.device("cpu")
        cfg.mixed_precision = False
        model = ProteinGeneratorModel(cfg).to(device)

    total_params = sum(param.numel() for param in model.parameters())
    print(f"Model parameters: {total_params:,}")

    try:
        history = train_model(model, train_loader, val_loader, cfg, device)
    except RuntimeError as exc:
        if device.type != "cuda" or not is_cuda_oom(exc):
            raise
        print("\nGPU memory was still not enough, even after automatic micro-batch retries.")
        print("Switching this run to CPU so it can finish.")
        print("For a faster GPU run, lower Context/block size first, then Pair embedding size,")
        print("Sequence embedding size, Transformer layers, or Batch size.")
        model = model.to("cpu")
        clear_cuda_memory()
        device = torch.device("cpu")
        cfg.mixed_precision = False
        history = train_model(model, train_loader, val_loader, cfg, device)

    graph_path = Path("luxr_training_loss_bar_graph.png")
    plot_loss_bar_graph(history, graph_path)

    print("\nGenerating final protein sequences...")
    generated = [generate_sequence(model, cfg, device) for _ in range(cfg.candidates)]
    ranked_candidates = rank_generated_sequences(model, generated, sequences, cfg, device)
    print_final_sequences(ranked_candidates)

    fasta_path, csv_path = save_outputs(ranked_candidates, Path("."))
    print("\nSaved final outputs:")
    print(f"FASTA: {fasta_path.resolve()}")
    print(f"CSV:   {csv_path.resolve()}")
    print(f"Graph: {graph_path.resolve()}")


if __name__ == "__main__":
    main()