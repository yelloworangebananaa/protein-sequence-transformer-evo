def ask_int(label: str, default: int, minimum: int | None = None) -> int:
    while True:
        try:
            value = input(f"{label} [{default}]: ").strip()
        except EOFError:
            return default
        if value == "":
            return default
        try:
            parsed = int(value)
            if minimum is not None and parsed < minimum:
                print(f"Use a value >= {minimum}.")
                continue
            return parsed
        except ValueError:
            print("Please enter a whole number.")


