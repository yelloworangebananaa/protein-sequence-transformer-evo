def ask_bool(label: str, default: bool) -> bool:
    default_text = "y" if default else "n"
    while True:
        try:
            value = input(f"{label} (y/n) [{default_text}]: ").strip().lower()
        except EOFError:
            return default
        if value == "":
            return default
        if value in {"y", "yes", "true", "1"}:
            return True
        if value in {"n", "no", "false", "0"}:
            return False
        print("Please enter y or n.")


